#include"libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c < 0 || c > 255)
	{
		while (c < 0)
			c += 256;
		while (c > 255)
			c -= 256;
	}
	while (*s && *s != c)
		s++;
	if (*s == c)
		return ((char *)s);
	return (NULL);
}
