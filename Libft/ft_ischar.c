#include"libft.h"

int	ft_ischar(const char *str, int c)
{
	if (c < 0 || c > 255)
	{
		while (c < 0)
			c += 256;
		while (c > 255)
			c -= 256;
	}
	while (*str)
	{
		if (*str == c)
			return (1);
		str++;
	}
	return (0);
}
